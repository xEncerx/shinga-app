/// An enumeration representing the different types of titles available in the system.
enum TitleType {
  /// Japanese manga.
  manga,

  /// Written novel.
  novel,

  /// Japanese light novel.
  lightNovel,

  /// A single-chapter, self-contained story.
  oneshot,

  /// A self-published or fan-made work.
  doujinshi,

  /// Korean manhwa.
  manhwa,

  /// Chinese manhua.
  manhua,

  /// Western-style comics.
  comics,

  /// Vertically scrolling digital comic.
  webtoon,

  /// A format that does not fit any other category.
  other,
}
